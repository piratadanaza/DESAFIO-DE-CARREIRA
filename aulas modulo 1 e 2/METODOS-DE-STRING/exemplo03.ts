// toUppercase --> transforma a string em caracteres maiusculos
// toLowercase --> transforma a string em caracteres minusculos

const texto = 'guido cerqueira'

console.log(texto);

//  tudo em maiusculo texto.toLocaleUpperCase()
const textoFormatado = texto.toLocaleUpperCase()

console.log(textoFormatado);

// tudo em minusculo
const textoFormatado2 = texto.toLowerCase()

console.log(textoFormatado2);

